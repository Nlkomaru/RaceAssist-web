export const getServerSideProps: GetServerSideProps = async (context) => {
  const token =
    process.env.RACEASSIST_API_USER + ":" + process.env.RACEASSIST_API_PASSWORD;
  const base64 = Buffer.from(token).toString("base64");
  const request = await fetch(process.env.RACEASSIST_API_URL + "list/name", {
    method: "GET",
    headers: {
      Authorization: "Basic " + base64,
    },
  });
  if (request.status !== 200) {
    return {
      props: {
        list: null,
      },
    };
  }

  const json = await request.json();
  const props: Props = {
    list: json,
  };
  return {
    props: props,
  };
};

export default Home;
